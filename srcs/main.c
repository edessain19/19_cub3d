#include "../include/cub3d.h"

int main(int argc, char **argv)
{
	data_t        data;

	if ((data.mlx_ptr = mlx_init()) == NULL)
		return (EXIT_FAILURE);
	if ((data.mlx_win = mlx_new_window(data.mlx_ptr, 640, 480, "Hello world")) == NULL)
		return (EXIT_FAILURE);
	ft_algo(argc, argv);
	mlx_loop(data.mlx_ptr);
	return (EXIT_SUCCESS);
}
