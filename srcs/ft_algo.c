void	pos_init(data_t *data)
{
	//position de depart
	data->pos->posX = 22; 
	data->pos->posY = 12;
	//direction de depart
	data->pos->dirX = -1;
	data->pos->dirY = 0;
	//vison de la camera
	data->pos->planeX = 0;
	data->pos->planeY = 0.66;
	//temps
	data->pos->time = 0;
	data->pos->oldtime = 0;
}

